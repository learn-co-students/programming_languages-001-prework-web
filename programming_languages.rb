def reformat_languages(languages)
  reformatted = {}

  languages.each do |style, languages_in_style|
    languages_in_style.each do |language_name, language_details|
      if reformatted[language_name]
        reformatted[language_name][:style] << style
      else
        reformatted[language_name] = {type: language_details[:type],
                                    style: [style]}
      end
    end
  end

  reformatted
end

